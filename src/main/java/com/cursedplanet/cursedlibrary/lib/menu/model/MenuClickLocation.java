package com.cursedplanet.cursedlibrary.lib.menu.model;

/**
 * Represents a click location while the menu is opened
 */
public enum MenuClickLocation {

	/**
	 * The upper menu was clicked
	 */
	MENU,

	/**
	 * The bottom player inventory was clicked
	 */
	PLAYER_INVENTORY,

	/**
	 * The arena out of GUI was clicked
	 */
	OUTSIDE
}